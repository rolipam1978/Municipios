public class MunicipioBean {
    
    private int coduf;
    private String siglauf;
    private int codmunic;
    private String municipio;

    /**
     * @return the coduf
     */
    public int getCoduf() {
        return coduf;
    }

    /**
     * @param coduf the coduf to set
     */
    public void setCoduf(int coduf) {
        this.coduf = coduf;
    }

    /**
     * @return the siglauf
     */
    public String getSiglauf() {
        return siglauf;
    }

    /**
     * @param siglauf the siglauf to set
     */
    public void setSiglauf(String siglauf) {
        this.siglauf = siglauf;
    }

    /**
     * @return the codminic
     */
    public int getCodmunic() {
        return codmunic;
    }

    /**
     * @param codminic the codminic to set
     */
    public void setCodmunic(int codmunic) {
        this.codmunic = codmunic;
    }

    /**
     * @return the municipio
     */
    public String getMunicipio() {
        return municipio;
    }

    /**
     * @param municipio the municipio to set
     */
    public void setMunicipio(String municipio) {
        this.municipio = municipio;
    }
    
    
}
